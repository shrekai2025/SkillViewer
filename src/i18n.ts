export type TranslationKey = keyof typeof messages['en-US']

export const SUPPORTED_LOCALES: SupportedLocale[] = ['zh-CN', 'en-US', 'ja-JP']

export function resolveAppLocale(value?: string | null): SupportedLocale {
  const normalized = (value ?? '').toLowerCase()

  if (normalized.startsWith('zh')) {
    return 'zh-CN'
  }
  if (normalized.startsWith('ja')) {
    return 'ja-JP'
  }

  return 'en-US'
}

export function getSystemLocale() {
  if (typeof navigator === 'undefined') {
    return 'zh-CN' satisfies SupportedLocale
  }

  return resolveAppLocale(navigator.language)
}

export function translate(
  locale: SupportedLocale,
  key: TranslationKey,
  params?: MessageParams,
) {
  const template = messages[locale][key] ?? messages['en-US'][key]
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_match, token) => {
    const value = params[token]
    return value === undefined ? '' : String(value)
  })
}

const formatterCache = new Map<SupportedLocale, Intl.DateTimeFormat>()

export function formatAppDate(locale: SupportedLocale, dateValue: string) {
  let formatter = formatterCache.get(locale)
  if (!formatter) {
    formatter = new Intl.DateTimeFormat(locale, {
      dateStyle: 'medium',
      timeStyle: 'short',
    })
    formatterCache.set(locale, formatter)
  }

  return formatter.format(new Date(dateValue))
}
